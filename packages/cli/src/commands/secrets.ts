export const secretsCommand = defineCommand({
  meta: {
    name: 'secrets',
    description: 'Secrets management commands',
  },
  subCommands: {
    list: listCommand,
    required: requiredCommand,
    get: getCommand,
    set: setCommand,
    audit: auditCommand,
  },
})
